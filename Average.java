public class Average {
	Average(){}

	public static final double average(final double n1, final double n2, final double n3, final double n4)
		{ return (n1 + n2 + n3 + n4) / 4; }

	public static final double average(final double n1, final double n2, final double n3)
		{ return (n1 + n2 + n3) / 3; }

	public static final double average(final double n1, final double n2)
		{ return (n1 + n2) / 2; }

};


